#include "cub3d.h"

void	ft_freemap(char **map)
{
	int	i;

	i = 0;
	if (map != 0)
	{
		while (map[i])
		{
			free(map[i]);
			map[i] = 0;
			i++;
		}
		free(map);
		map = 0;
	}
}
